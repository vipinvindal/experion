package com.bets.experion.mvc.form;


public class WalkthroughDTO {

	private String projectName;
	private String projectId;
	
	private String walkThroughId;
	private String walkThroughName;
	private String walkThroughLink;
	public String getProjectName() {
		return projectName;
	}
	public void setProjectName(String projectName) {
		this.projectName = projectName;
	}
	public String getProjectId() {
		return projectId;
	}
	public void setProjectId(String projectId) {
		this.projectId = projectId;
	}
	public String getWalkThroughId() {
		return walkThroughId;
	}
	public void setWalkThroughId(String walkThroughId) {
		this.walkThroughId = walkThroughId;
	}
	public String getWalkThroughName() {
		return walkThroughName;
	}
	public void setWalkThroughName(String walkThroughName) {
		this.walkThroughName = walkThroughName;
	}
	public String getWalkThroughLink() {
		return walkThroughLink;
	}
	public void setWalkThroughLink(String walkThroughLink) {
		this.walkThroughLink = walkThroughLink;
	}
	
	
	
	
}


